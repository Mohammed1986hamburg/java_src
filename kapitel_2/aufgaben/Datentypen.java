package kapitel_2.aufgaben;

public class Datentypen {

	public static void main(String[] args) {
		
		boolean a = true;
		System.out.println("Wert fur boolean: "+ a);
		
		boolean b = false;
		System.out.println("Wert fur boolean"+ b);
		
		char h = 'H';
		System.out.println("char: "+ h);
		
		byte byt = 100;
		System.out.println("hoechster Wert fur byte: "+ Byte.MAX_VALUE);
		System.out.println("kleinster Wert fur short: "+ Byte.MIN_VALUE);
		
		short shorT = 101;
		System.out.println("hoechster Wert fur short:"+ Short.MAX_VALUE);
		System.out.println("kleinster Wert fur short"+ Short.MIN_VALUE);
		
		int inT = 10;
		System.out.println("hoechster Wert fur int:"+ Integer.MAX_VALUE);
		System.out.println("kleinster Wert fur int"+ Integer.MIN_VALUE);
		
		long lonG = 1524l;
		System.out.println("hoechster Wert fur int:"+ Long.MAX_VALUE);
		System.out.println("kleinster Wert fur int"+ Long.MIN_VALUE);
		
		float floaT = 7.123457f;     // frage
		System.out.println("float = "+ floaT);
		System.out.println("hoechster Wert fur float:"+ Float.MAX_VALUE);
		System.out.println("kleinster Wert fur float"+ Float.MIN_VALUE);
		
		double doublE = 7.123456789012346;
		System.out.println("float = "+ doublE);
		System.out.println("hoechster Wert fur double:"+ Double.MAX_VALUE);
		System.out.println("kleinster Wert fur double"+ Double.MIN_VALUE);
		
		
		
		
		
		
		
		
		
		

	}

}
